package pkg01;

public class Switch01 {
	public static void main(String args[]){
		int age = 3;
		
		switch (age){
		case 1:
			System.out.println("Ur age is 1");
			break;
		case 2:
			System.out.println("ur age is 2");
			break;
		case 3:
			System.out.println("ur age is 3");
			break;
		case 4:
			System.out.println("ur age is 4");
		default:
			System.out.println("Dont know ur age.");
		    
		}
	}
}
