package pkg01;

public class Switch02 {
	public static void main(String args[]){
		char itm = 'p';
		
		switch (itm){
		case 'a':
			System.out.println("10");
			break;
		case 'b':
			System.out.println("20");
			break;
		case 'p':
			System.out.println("15");
			break;
		default:
			System.out.println("check the entry.");
		}
	}
}
